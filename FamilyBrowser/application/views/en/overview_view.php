    <p>
        The component catalogs are managed and maintained in a database and are permanently integrated into the
        application. <br>
        This ensures that the modelers in the projects always use the same component definitions,
        which significantly promotes the implementation of model standards in the company. <br>
        The system-centric approach of the RevitFamilyBrowser (read: Software & Content Coupling) allows you to extend functionality for optimal
        support <br> of the specific BIM application targets in the 4D / 5D / 6D / xD range. <br> The customer thus benefits from
        continuous increases in value, which simplifies BIM planning and increases quality of the work results.
