<div class="article-content">
      <p>
            To find desired element you should make 4 steps: <br>
            1. Select category in Ribbon. <br>
            2. Select mount type Flush or Surface setting a filter. <br>
            3. Select placement Ceiling / Wall / Floor setting a filter (you can choose more than one option). <br>
            4 Select Installation Medium. <br>
      </p>
      <p>
            To insert element into the project you need to:
            <ul>
                  <li>Make double-click on desired element and place it in your project, or</li>
                  <li>Drag element from main panel and drop into your project</li>
            </ul>
      </p>

