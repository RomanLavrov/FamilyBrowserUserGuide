<div class="article-header">
    Registration / login
</div>
<div class="article-content">
    <p>With the FamilyBrowser version 2022, we are introducing the BUILDing360 account,
        which can be used as single login for all existing and especially future BUILDing360 services,
        such as the BUILDing360.CMS (BIM Content Management System),
        BUILDing360.Cockpit (Building Information System) or the BUILDing360 Community.</p>
    <p class="font-weight-bold">The FamilyBrowser can thus only be used with a valid BUILDing360 account as of version 2022.</p>
</div>

<div class="article-header">
    Registration
</div>
<div class="article-content">
    <p>After installing FamilyBrowser version 2022, all functions are initially locked.
        When clicking on any button within the FamilyBrowser ribbon,
        a pop-up appears asking you to log in:</p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/login-window_en.png" alt="">
    </div>
    <br />
    <p>
        Directly within the pop-up you will find a link
        that leads to the registration page <a href="https://account.building360.ch/register">(Create BUILDing360 account)</a>,
        where a short form has to be filled in:
    </p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/registration_en.png" alt="">
    </div>
    <p>
        <br />A few minutes after submitting the form, an email will be sent to the address provided.
        <br />It contains a link that must be clicked in order to verify the personal account.
        <br />As soon as the verification has been successful, the registration is complete and you can log in to the FamilyBrowser.
    </p>
</div>
<div class="article-header">
    Login and logout in the FamilyBrowser
</div>
<br />
<div class="article-header-small">
    Login
</div>
<div class="article-content">
    <p>If you already have a BUILDing360 account, you can find the login icon in the FamilyBrowsers Ribbon under "Help": </p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/ribbon-login-button_en.png" alt="">
    </div>
    <p> <br />
        Once logged in, your login is valid across all FamilyBrowser catalogues
        (architecture, electrical, HVAC) and it will be saved
        so that you do not need to log in again after a restart.
    </p>
</div>

<div class="article-header-small">
    Password forgotten
</div>
<div class="article-content">
    If you can’t remember your password, click the button right next to the password input field to reset your password or do it directly on the
    <a href="https://account.building360.ch/password/reset">BUILDing360 password reset page</a>.
</div>

<div class="article-header-small">
    Logout
</div>
<div class="article-content">
    <p>To log out, click on the same button that is used for the login.
        The current logged-in account will then be displayed:</p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/logout-window_en.png" alt="">
    </div>
    <br />
    <p>Click on the "Logout" button to execute the logout. </p>
</div>

<div class="article-header">
    Manage account
</div>
<div class="article-content">
    <p>You can manage your own account on the
        <a href="https://account.building360.ch/app/Account">BUILDing360 account management page</a>
        under the menu item “Account”:
    </p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/account_en.png" alt="">
    </div>
</div>

<div class="article-header-small">
    Edit personal data
</div>
<div class="article-content">
    <p>To edit the personal data, overwrite the fields with the existing data.
        <br />Click the “Save” button to apply changes.
    </p>
</div>

<div class="article-header-small">
    Manage newsletter settings
</div>
<div class="article-content">
    <p>The newsletter checkmark can be set to receive the newsletter or removed to unsubscribe from it.
        <br />To apply changes, click the “Save” button.
    </p>
</div>

<div class="article-header-small">
    Delete account
</div>
<div class="article-content">
    <p>Clicking the “Delete” button opens a window where the account deletion must be confirmed.
        <br />After that the account and all personal data will be removed.
    </p>
    <br />
    <p>
        <span class="font-weight-bold">Attention:</span> With the deletion of the account, the use of the FamilyBrowser (from version 2022) is no longer possible!
        <br />The account cannot be restored, a new registration is required.
    </p>
            </br>
</div>


