      <p>
            Die Bauteilkataloge sind als eine Datenbank verwaltet und gepflegt. Sie sind permanent in die Anwendung
            integriert. <br>
            Dadurch wird sichergestellt, dass die Modellierer in den Projekten immer dieselben Bauteildefinitionen
            verwenden, was schlussendlich die Implementierung von Modellstandardisierung im Unternehmen fördert. <br>
            Der systemorientierte Ansatz von RevitFamilyBrowser ermöglicht die Erweiterung der Revit-Funktionalität 
            für eine optimale Unterstützung der spezifischen
            BIM-Anwendungsziele im 4D / 5D / 6D / xD-Bereich. <br> Der Kunde
            profitiert somit von einer kontinuierlichen Wertsteigerung, welche die BIM-Planung vereinfacht und die
            Qualität der Arbeitsergebnisse erhöht.
