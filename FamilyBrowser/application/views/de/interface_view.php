<div class="article-header">
      Hauptfenster
</div>
<div class="article-content">
      <p>
            Das Hauptfenster kann in die vorhandenen Revit-Fenster integriert werden, z. B. "Parameter" oder
            "Projektbrowser". Dieses Fenster enthält Filter und eine Liste der verfügbaren Typen und ladet die ins
            Projekt.
      </p>
      <div class="row">
            <div class="col-md-5">
                  <img style="width:100%;" class="illustration" src="/FamilyBrowser/images/Dockable_de.png" alt="">
            </div>
            <div class="col-md-7">
                  <div class="article-header">Navigation</div>
                  <div class="article-content">Der Name der aktuellen Kategorie wird im Feld "Kategorie" über der
                        Tabelle angezeigt.</div>
                  <div class="article-header">Filter</div>
                  <div class="article-content">Basierend auf ihren internen Parametern gibt es drei Filter für
                        Komponenten:
                        <p>
                              1. Unterputz / Aufputzmontage; <img src="/FamilyBrowser/images/filterMount_de.png"
                                    alt=""><br />
                              2. Platzierung an Decke / Wand / Boden;<img src="/FamilyBrowser/images/filterPlace_de.png"
                                    alt=""> <br>
                              3. Installationsmedium (Liste der Systeme hängt vom ausgewählten System ab);
                        </p>
                        <p>
                              Sobald der Auswahlfilter eingestellt ist, werden verwandte Elemente und Elemente ohne
                              solche Parameter in einer Liste angezeigt.<br>
                              Mehrere Filter können gleichzeitig angewendet werden. Z.B. Sie können Flush-Mount-Elemente
                              für das 400-V-System an der Wand auswählen.<br>
                              Um die Filterauswahl aufzuheben, klicken Sie erneut darauf.
                  </div>
            </div>
