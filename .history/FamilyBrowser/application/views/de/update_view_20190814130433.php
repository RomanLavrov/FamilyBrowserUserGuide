<div class="article-header">
      Aktualisierung
</div>
<div class="article-content" style="margin-bottom:-15px;">
      <p>
            <img src="/FamilyBrowser/images/UpdateFamily.png" alt="">
            Aktualisierung ermöglicht die neueste Version von Add-In-Komponenten zu erhalten:
            <ul>
                  <li>1. IFC-Export</li>
                  <li>2. Projektvorlage</li>
                  <li>3. Executive-Paket</li>
            </ul>
            <p>
                  Komponenten werden separat aktualisiert, falls eine neuere Version verfügbar ist. Um das Executive-Paket zu
                  aktualisieren, muss man zuerst Revit schliessen.
            </p>
            <p>
                  Die neuen oder korrigierten Familien werden im Family Browser automatisch erscheinen. Falls eine neue Version vom Bauteilkatalog zur Verfügung steht, bekommen Sie eine Meldung. Die neue Version kann dann über den Knopf "Building360 aktualisieren" heruntergeladen werden.
            </p>

      </p>
</div>
