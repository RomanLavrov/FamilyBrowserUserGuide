<div class="article-header">
    Registrierung / Login
</div>

<div class="article-content">
    <p>Mit der FamilyBrowser Version 2022 wird das BUILDing360 Konto eingeführt,
        welches als zentrales Login für alle bestehenden und besonders auch künftigen BUILDing360
        Dienste verwendet werden kann, wie BUILDing360.CMS (BIM Content Management System),
        BUILDing360.Cockpit (Gebäudeinformationssystem) oder die BUILDing360 Community.</p>
    <p class="font-weight-bold">Der FamilyBrowser kann ab Version 2022 daher nur mit gültigem BUILDing360 Konto verwendet werden.</p>
</div>

<div class="article-header">
    Registrierung
</div>
<div class="article-content">
    <p>Nach der Installation der FamilyBrowser Version 2022 sind initial alle Funktionen gesperrt.
        Beim Klick auf einen beliebigen Button innerhalb des FamilyBrowser Ribbons erscheint ein Popup,
        das zum Einloggen auffordert: </p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/login-window_de.png" alt="">
    </div>
    <br />
    <p>
        Direkt innerhalb des Popups befindet sich ein Link, der zur Registrierungsseite <a href="https://account.building360.ch/register">(BUILDing360 Konto erstellen)</a>,
        führt, wo ein kurzes Formular zur ausgefüllt werden muss:
    </p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/registration_de.png" alt="">
    </div>
    <p>
        Wenige Minuten nach dem Absenden des Formulars wird eine E-Mail an die angegebene Adresse versendet.
        Diese enthält einen Link, welcher angeklickt werden muss, um das persönliche Konto zu verifizieren.
        Sobald die Verifizierung erfolgreich war, ist die Registrierung abgeschlossen – dem Login im FamilyBrowser steht nichts mehr im Weg.
    </p>
</div>
<div class="article-header">
    Login und Logout im FamilyBrowser
</div>
<br />
<div class="article-header">
    Login
</div>
<div class="article-content">
    <p>Wenn bereits ein BUILDing360 Konto angelegt wurde, kann im FamilyBrowser Ribbon unter «Hilfe» das Icon zum Login angeklickt werden: </p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/ribbon-login-button_de.png" alt="">
    </div>
    <p> <br />
        Wer sich einmal eingeloggt hat, ist über alle FamilyBrowser Kataloge hinweg (Architektur, Elektro, HLKS)
        eingeloggt und braucht sich auch nach einem Neustart nicht mehr erneut einzuloggen.
    </p>
</div>

<div class="article-header">
    Passwort vergessen
</div>
<div class="article-content">
    Du kannst dich nicht mehr an dein Passwort erinnern? Klicke auf den kleinen Button rechts neben dem Eingabefeld für das Passwort, um dein Passwort zurückzusetzen oder mache es direkt auf der
    <a href="https://account.building360.ch/password/reset">BUILDing360 Passwort zurücksetzen Seite</a>.
</div>

<div class="article-header">
    Logout
</div>
<div class="article-content">
    <p>Um sich auszuloggen muss einmal auf denselben Button geklickt werden, der für das Login verwendet wird.
        Danach erscheint die Ansicht des aktuell eingeloggten Kontos: </p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/logout-window_de.png" alt="">
    </div>
    <br />
    <p>Mit einem Klick auf den «Logout»-Button wird das Logout ausgeführt.</p>
</div>

<div class="article-header">
    Konto verwalten
</div>
<div class="article-content">
    <p>Auf der
        <a href="https://account.building360.ch/app/Account">BUILDing360 Account Management</a>
        Seite kann der eigene Account unter dem Menüpunkt «Konto» verwaltet werden:
    </p>
    <div class="d-flex justify-content-center modal-img-container">
        <img class="search-imgs" src="/FamilyBrowser/images/account_de.png" alt="">
    </div>
</div>

<div class="article-header">
    Persönliche Daten bearbeiten
</div>
<div class="article-content">
    <p>Um die persönlichen Daten zu bearbeiten können die Felder mit den bestehenden Daten einfach überschrieben werden.
        <br />Zum Übernehmen der Änderungen auf den «Speichern»-Button klicken.
    </p>
</div>

<div class="article-header">
    Newslettereinstellungen verwalten
</div>
<div class="article-content">
    <p>Das Häkchen für den Newsletter kann gesetzt werden, um den Newsletter zu empfangen oder entfernt werden, um sich davon abzumelden.
        <br />Zum Übernehmen von Änderungen auf den «Speichern»-Button klicken.
    </p>
</div>

<div class="article-header">
    Konto löschen
</div>
<div class="article-content">
    <p>Mit einem Klick auf den «Löschen»-Button öffnet sich ein Fenster, wo die Löschung des Accounts bestätigt werden muss.
        <br />Danach wird der Account mit allen persönlichen Daten entfernt.
    </p>
    <br />
    <p>
        <span class="font-weight-bold">Achtung:</span> Mit der Löschung des Accounts ist auch die Nutzung des FamilyBrowsers (ab Version 2022) nicht mehr möglich!
        <br />Der Account kann nicht wiederhergestellt werden, es ist eine Neuregistrierung erforderlich.
    </p>
    </br>
</div>


