<div class="article-header">Versionen</div>
<div class="article-content">
      <div class="row">
            <div class="col-md-5" style="min-width:480px">
                  <img style="margin-left:0" class="illustration" src="/FamilyBrowser/images/Tab_Version_de.png"
                        alt="" />
            </div>
            <div class="col-md-5">
                  <p>
                        Auf der Registerkarte "Version" können Sie die Familienversion ändern, welche Sie in Ihrem Projekt verwenden möchten. 
                        Für jede Version sehen Sie das Erscheinungsdatum der Familien und eine kurze Beschreibung dieser Version. 
                        Ein Algorithmus erlaubt alle BUILDing360-Familien, die im aktuellen Projekt verwendet wurden, auf die ausgewählte 
                        Version zu ändern. Weitere Informationen finden Sie <a href="/FamilyBrowser/de/Update">hier</a>.</p>
                  <p>
                        Versionsbeschreibung:
                  </p>
                  <ul>
                        <li>0.9.0 - erste Version der generischen elektrischen Familien. Hat kein Vorschaubild.</li>
                        <li>1.0.0 - Elektro- und HLKS-Familien mit Vorschaubild.</li>
                        <li>2.0.0 - aktualisierte Familien mit den Absteckpunkten für die Laserpositionierung vor Ort.</li>
                  </ul>
                  <p>
                        Es wird empfohlen, die neueste Familienversion zu verwenden, ausser wenn Ihr Projekt mit älteren 
                        Familienversionen erstellt wurde und weitere Updates nicht gewünscht sind. 
                        Der Aktualisierungsvorgang überträgt benutzerdefinierte Eigenschaften zwischen verschiedenen 
                        Versionen, sodass die Informationen nicht verloren gehen.
                  </p>
            </div>
      </div>
</div>