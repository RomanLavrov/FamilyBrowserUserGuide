<div class="article-header">Versionen</div>
<div class="article-content">
      <div class="row">
            <div class="col-md-5" style="min-width:480px">
                  <img style="margin-left:0" class="illustration" src="/FamilyBrowser/images/Tab_Version_en.png"
                        alt="" />
            </div>
            <div class="col-md-5">
            Auf der Registerkarte "Version" können Sie die Familienversion ändern, die Sie in Ihrem Projekt verwenden möchten.
                  Für jede Version sehen Sie das Erscheinungsdatum der Familien und eine kurze Beschreibung dieser Version.
                  Es gibt ein Tool, das es erlaubt, alle BUILDing360-Familien, die im aktuellen Projekt verwendet wurden, auf Version zu ändern.
                  die in dieser Registerkarte ausgewählt wurde. Weitere Informationen finden Sie unter <a href="/FamilyBrowser/en/Update">hier</a>.
                  <p>
                        Versionsunterschied:
                  </p>
                  <ul>
                        <li>0.9.0 - initial version of generic electrical familes. Has no preview image.</li>
                        <li>1.0.0 - electrical and HVAC families with preview image.</li>
                        <li>2.0.0 - updated families with location point for laser-positioning on site.</li>
                  </ul>
                  <p>
                        It is recommended to use the latest families version, unless your project is made with older
                        version and you do not need to have extra parameters. Update process transfers custom properties
                        between different type versions.
                  </p>
            </div>
      </div>
