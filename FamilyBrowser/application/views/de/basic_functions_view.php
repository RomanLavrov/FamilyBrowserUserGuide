<div class="article-content">
      <p>
            Um das gewünschte Element zu finden, müssen Sie 4 Schritte durchführen: <br>
            1. Wählen Sie die Kategorie in der Multifunktionsleiste ausn. <br>
            2. Wählen Sie den Mount-Typ Flush oder Surface, indem Sie auf den Filter drücken. <br>
            3. Wählen Sie Platzierung Decke / Wand / Boden, indem Sie auf den Filter drücken (Sie können mehrere
            Optionen auswählen).<br>
            4. Wählen Sie ein Installationsmedium. <br>
      </p>
      <p>
            Um ein Element ins Projekt einzufügen, sind folgende Schritte notwendig:
            <ul>
                  <li>Machen Sie einen Doppelklick auf das gewünschte Element und platzieren Sie es in Ihrem Projekt
                        oder</li>
                  <li>ziehen Sie das Element aus dem Hauptfenster und legen Sie es in Ihrem Projekt ab.</li>
            </ul>
      </p>

