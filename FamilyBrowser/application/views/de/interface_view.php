<div class="article-header">
      Hauptfenster
</div>
<div class="article-content">
      <p>
            Das Hauptfenster kann in die vorhandenen Revit-Fenster integriert werden, z. B. 
            "Parameter" oder "Projektbrowser". Das Hauptfenster enthält Filter und eine Liste der 
            verfügbaren Typen und ladet die ins Projekt.
      </p>
      <div class="row">
            <div class="col-md-5">
                  <img style="width:100%;" class="illustration" src="/FamilyBrowser/images/RFB_Panel_de.png" alt="">
            </div>
            <div class="col-md-7">
                  <div class="article-header">Navigation</div>
                  <div class="article-content">Oben in der Tabelle sind der aktuelle Kategoriename und die aktuell ausgewählte Version angezeigt.</div>
                  <div class="article-header">Filter</div>
                  <div class="article-content">
                        <p>
                              Je nach enthaltene Parameter kann man die Familien auf folgende Weise filtern oder suchen:
                        </p>
                        <p>
                              <ol>
                                    <li>Unterputz / Aufputzmontage;</li>
                                    <li>Platzierung an Decke / Wand / Boden;</li>
                                    <li>Installationsmedium (Liste der Systeme hängt vom ausgewählten System ab);</li>
                                    <li>Suchleiste, welche die aktuellen Familientypen nach Ihrer Eingabezeichenfolge filtert 
                                    (verfügbar in Projectfamilien, Familien-Aktualisierung, Produktfamilien).</li>
                              </ol>
                        </p>
                        <div class="d-flex justify-content-center" style="margin: 1em 0 1em 0;">
                              <img src="/FamilyBrowser/images/filters_panel_de.PNG" alt="filters">
                        </div>
                        <p>
                              Sobald ein Filter eingestellt ist, werden verwandte Elemente und Elemente ohne solche Parameter in einer Liste angezeigt.<br>
                              Mehrere Filter können gleichzeitig angewendet werden. Z.B. Sie können Flush-Mount-Elemente für das 400-V-System an der Wand auswählen.<br>
                              Um die Filterauswahl aufzuheben, klicken Sie erneut darauf.
                        </p>
                  </div>
            </div>
