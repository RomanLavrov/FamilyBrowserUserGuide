<div class="article-header-small">NEW</div>
<div class="article-content">
    <p>Architecture catalogue 1.0.0</p>
    <p>First version of architectural components with the usual BUILDing360 standard.</p>
    <br />
    <p>Electrical switch configurator</p>
    <p>Simple composition of switch combinations using drag and drop with automatic placement relative to the selected door.</p>
</div>

<div class="article-header-small">IMPROVEMENTS</div>
<div class="article-content">
    <p class="mb-1">Electrical catalogue 2.2.0</p>
    <dl style=" list-style-type: none">
        <li>- New components for hospital applications</li>
        <li>- Clear socket symbols causing less text overlap on plans</li>
        <li>- Cable support systems with new grid channel system, additional parameter data and optimized display on 2D plans</li>
        <li>- Path labels with automatic height and width as well as display of function maintenance</li>
    </dl>

    <p class="mb-1">HLKS catalogue 3.0.0</p>
    <dl style="list-style-type: none">
        <li>- New air diffusers, air duct fittings as well as sanitary installations, sanitary pipe fittings and sanitary pipe accessories</li>
        <li>- Solving problems with ventilation components</li>
        <li>- Revision of heating manifold components, addition of parameter</li>
        <li>- Optimisation of the template, especially the views and labels</li>
        <li>- New legends</li>
    </dl>

    <p class="mb-1">Recess labels</p>
    <p>Automatic dimension and height information for all catalogues.</p>

    <p class="mb-1">Tab titles</p>
    <p>Shorter tab titles for the individual catalogues enable the display of more plug-ins in the Revit menu bar without having to expand the menu.</p>
</div>

<div class="article-header-small">BUG FIXING</div>
<div class="article-content">
    <p>Performance issues</p>
    <p>Whenever the FamilyBrowser has been installed but not been used since opening Revit, long loading times occurred when switching between projects or trying to edit a component family. The bug has been solved, performance is back to normal.</p>
</div>

