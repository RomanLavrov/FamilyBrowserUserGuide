<div class="article-header" id="main-panel">
      Main Panel
</div>
<div class="article-content">
      <p>
            Main Panel can be incorporated to the existing Revit panels such as "Parameters" or "Project Browser". 
            This panel contains filters, list of available types and allows to load them into project.
      </p>
      <div class="row">
            <div class="col-md-5">
                  <img style="width:100%;" class="illustration" src="/FamilyBrowser/images/RFB_Panel_en.png" alt="">
            </div>
            <div class="col-md-7">
                  <div class="article-header">Navigation</div>
                  <div class="article-content">
                        Current category name is displayed in the "Category" field on top of the table along with the current selected version.
                  </div>
                  <div class="article-header">Filters</div>
                  <div class="article-content">
                        <p>
                              There are three main filters and one optional for element based on their internal parameters:
                        </p>
                        <p>
                              <ol>
                                    <li>Flush Mount / Surface Mount;</li>
                                    <li>Placement on Ceiling / Wall / Floor;</li>
                                    <li>Installation Medium (list of systems depends on selected system);</li>
                                    <li>Search bar which filters current family types by your input string (available in Used Families, Families Update, Vendor Families).</li>
                              </ol>
                        </p>
                        <div class="d-flex justify-content-center" style="margin: 1em 0 1em 0;">
                              <img src="/FamilyBrowser/images/filters_panel_en.PNG" alt="filters">
                        </div>
                        <p>
                              Once selection filter is activated, related items and items without such parameter will be displayed in a list.<br>
                              Multiple filters may work together - you can choose Flush Mount elements for placing on Wall for 400V system.<br>
                              Filters are toggle buttons, i.e. to unselect filter - click it again.
                        </p>
                  </div>
