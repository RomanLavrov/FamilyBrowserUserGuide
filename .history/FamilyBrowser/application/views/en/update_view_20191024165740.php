<div class="article-header">
      Update
</div>
<div class="article-content" style="margin-bottom:-15px;">
      <p>
            <img src="/FamilyBrowser/images/UpdateFamily.png" alt="">
            Update allows to get the newest version of addin components:
            <ul>
                  <li>1. IFC Export</li>
                  <li>2. Project template</li>
                  <li>3. Executive package</li>
            </ul>

            <p>
                  Components are being updated separately if newer version is available.
                  To update executive package you should close Revit first.
            </p>
            <p>
                  The new or corrected families will automatically appear in the Family Browser. If a new version of the
                  parts catalog is available, you will get a message. The new version can then be downloaded via the
                  button "Update Building360".
            </p>
      </p>
</div>

