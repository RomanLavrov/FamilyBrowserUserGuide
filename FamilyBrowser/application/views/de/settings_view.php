<div class="article-header">Versionen</div>
<div class="article-content">
      <div class="row">
            <div class="col-md-5" style="min-width:480px">
                  <img style="margin-left:0" class="illustration" src="/FamilyBrowser/images/Tab_Version_de.png"
                        alt="" />
            </div>
            <div class="col-md-5">
                  <p>
                        Auf der Registerkarte "Versionen" ist es möglich, die Version der Familien für jedes Gewerk, 
                        das Sie in Ihrem Projekt verwenden, separat zu ändern. Für alle Versionen können Sie das 
                        Veröffentlichungsdatum der Familien und eine kurze Beschreibung dieser Version als Tooltip 
                        im Dropdown-Menü ansehen. Ausserdem ist es möglich die Version der einzelnen Familien, die 
                        im aktuellen Projekt verwendet wurden, zu ändern. Weitere Informationen dazu finden Sie 
                  <a href="/FamilyBrowser/de/Update">hier</a>.</p>
                  <p>
                        Versionsbeschreibung:
                  </p>
                  <ul>
                        <li>0.9.0 - erste Version der generischen elektrischen Familien. Vorschaubilder nicht vorhanden.</li>
                        <li>1.0.0 - Elektro- und HLKS-Familien mit den Vorschaubildern.</li>
                        <li>2.0.0 - aktualisierte Familien mit den Absteckpunkten für die Laserpositionierung bei Montage.</li>
                  </ul>
                  <p>
                        Es wird empfohlen, die neueste Version der Familien zu verwenden, sofern Ihr Projekt nicht mit einer älteren Version erstellt wurde und Sie keine zusätzlichen Parameter wünschen. Die benutzerdefinierten Eigenschaften werden während der Änderung der Familienversion behalten.
                  </p>
            </div>
      </div>
</div>