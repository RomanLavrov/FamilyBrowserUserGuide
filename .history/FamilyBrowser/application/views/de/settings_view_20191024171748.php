<div class="article-header">Versionen</div>
<div class="article-content">
      <div class="row">
            <div class="col-md-5" style="min-width:480px">
                  <img style="margin-left:0" class="illustration" src="/FamilyBrowser/images/Tab_Version_de.png"
                        alt="" />
            </div>
            <div class="col-md-5">
                  <p>
                        Auf der Registerkarte "Version" können Sie die Familienversion ändern, die Sie in Ihrem Projekt
                        verwenden möchten.
                        Für jede Version sehen Sie das Erscheinungsdatum der Familien und eine kurze Beschreibung dieser
                        Version.
                        Es gibt ein Tool, das es erlaubt, alle BUILDing360-Familien, die im aktuellen Projekt verwendet
                        wurden, auf Version zu ändern.
                        die in dieser Registerkarte ausgewählt wurde. Weitere Informationen finden Sie unter <a
                              href="/FamilyBrowser/en/Update">hier</a>.</p>
                  <p>
                        Versionsunterschied:
                  </p>
                  <ul>
                        <li>0.9.0 - erste Version der generischen elektrischen Familien. Hat kein Vorschaubild.</li>
                        <li>1.0.0 - Elektro- und HLKS-Familien mit Vorschaubild.</li>
                        <li>2.0.0 - aktualisierte Familien mit Ortungspunkt für die Laserpositionierung vor Ort.</li>
                  </ul>
                  <p>
                        Es wird empfohlen, die neueste Familienversion zu verwenden, es sei denn, Ihr Projekt wird mit
                        älteren Versionen erstellt.
                        Version und Sie müssen keine zusätzlichen Parameter haben. Der Aktualisierungsprozess überträgt
                        benutzerdefinierte Eigenschaften
                        zwischen verschiedenen Typversionen.
                  </p>
            </div>
      </div>
