<div class="article-header">
      Update
</div>
<div class="article-content" style="margin-bottom:-15px;">
      <p>
            <img src="/FamilyBrowser/images/UpdateFamily.png" alt="">
                  Update allows to get the newest version of plug-in components via single .msi package:
            <ul>
                  <li>IFC Export</li>
                  <li>Project template</li>
                  <li>Executive package</li>
            </ul>

            <p>
                  Such components as IFC export and Project template may be updated separately without .msi package using 
                  <a href="/FamilyBrowser/en/Interface#template-id">“Import BUILDing360 Template”</a> button. 
                  To update executive package don’t forget to shut down Revit.
            </p>
            <p>
                  New or updated families will appear automatically in Family Browser. If a new version of plugin is available, 
                  you will get notified. An update may be critically important, thus you should download recent .msi package as 
                  soon as possible. A new version can be downloaded via the button "Plugin update" or <a href="http://software.mum.ch/Plugins/">here</a>.
            </p>
      </p>
</div>

<div class="article-header">Problems with updates or updating from version < 2.0.8</div>
<div class="article-content" style="margin-bottom:-15px;">
      <p>
            If there are any problems with native update through button “Plugin update” you should 
            <a href="https://support.microsoft.com/en-us/help/4028054/windows-10-repair-or-remove-programs">manually uninstall</a> your 
            current Revit Family Browser and download .msi package <a href="http://software.mum.ch/Plugins/">here.</a>
      </p>
      <p>
             If you are using FamilyBrowser of version below 2.0.8, please, repeat the steps described above.
      </p>
      <p>
            <i>Contents (i.e. family files) via the internal update button will be no longer updated since version 2.0.0.</i>
      </p>
</div>

<div class="article-header">Update Families Version</div>

<div class="article-content" style="margin-bottom:-15px;">
      <div class="d-flex justify-content-start">
            <div class="p-2" style="padding-left:0px!important;">
                  <div>
                        <img class="version-img" src="/FamilyBrowser/images/FamiliesVersion_en_1a.png" />
                        <div class="d-flex justify-content-center"> Fig. 1a </div>
                        <img class="version-img" src="/FamilyBrowser/images/FamiliesVersion_en_1b.png" />
                        <div class="d-flex justify-content-center"> Fig. 1b </div>
                        <img class="version-img" src="/FamilyBrowser/images/FamiliesVersion_en_1c.png" />
