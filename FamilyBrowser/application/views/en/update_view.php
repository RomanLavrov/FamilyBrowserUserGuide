            </div>
            <div class="p-2" style="padding-left:3em!important;">
                  <p>First of all select families version you want to use in <a href="/FamilyBrowser/en/Settings">Settings</a>.</p>
                  <p>To change families version within project you need to press the button 
                        <img style="height:64px" src="/FamilyBrowser/images/FamiliesUpdateButton_en.png" />. 
                        This will collect all used family types in project and show them in the main Family Browser panel. 
                        The collection process may take some time in large projects, but it can be cancelled anytime. 
                        Each family type, created by BUILDing360 has a preview image, description and version (fig. 1a). 
                        You can filter Families by version using drop-down filter (fig. 1b) or via internal parameters in 
                        the search bar (fig. 1c). 
                  </p>
                  <p>By pressing "Update all" or by selecting checkbox near this button and then pressing it - you can change version of all families within current project.</p>
                  <p>All custom properties will be transferred automatically during update process and they will not be lost.</p>
                  <p>Families version may be either upgraded or downgraded. Simply set preferred version in <a href="/FamilyBrowser/en/Settings">Settings</a> and update it in “Families update”.</p>
                  <p>Version 0.9.0 doesn't have preview images.</p>
                  <p>
                        Present types in families may vary between different versions. If you used version 1.0.0 and can't find a specific type in version 2.0.0 - 
                        you have to set version 1.0.0 in Settings or use another elements.
                  </p>
            </div>
      </div>
