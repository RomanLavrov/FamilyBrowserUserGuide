<div class="article-header-small">NEU</div>
<div class="article-content">
    <p>Architekturkatalog 1.0.0</p>
    <p>Lancierung von Architekturbauteilen im gewohnten BUILDing360-Standard.</p>
    <br />
    <p>Elektro-Schalterkonfigurator </p>
    <p>Zusammenstellung von Schalterkombinationen mit Drag und Drop sowie automatischer Platzierung relativ zur ausgewählten Tür.</p>
</div>

<div class="article-header-small">VERBESSERUNGEN</div>
<div class="article-content">
    <p class="mb-1">Elektrokatalog 2.2.0</p>
    <dl style=" list-style-type: none">
        <li>- Neue Bauteile für Spitalanwendungen</li>
        <li>- Klare Steckdosensymbole mit weniger Text-überlappung auf Plänen</li>
        <li>- Kabeltragsysteme mit neuem Gitterkanalsystem, vervollständigten Parameterdaten sowie optimierter Darstellung auf 2D-Plänen</li>
        <li>- Trassebeschriftungen mit automatischer Höhe und Breite sowie Anzeige des Funktionserhalts </li>
    </dl>

    <p class="mb-1">HLKS-Katalog 3.0.0</p>
    <dl style="list-style-type: none">
        <li>- Neue Luftdurchlässe, Luftkanalformteile, Sani-tärinstallationen, Sanitär-Rohrformteile sowie
            -Rohrzubehör</li>
        <li>- Fehlerbehebung bei Lüftungsbauteilen</li>
        <li>- Überarbeitung der Heizverteilerbauteile, Ergänzung von Parameter</li>
        <li>- Optimierung der Vorlage, insbesondere die Ansichten und Bezeichnungen</li>
        <li>- Neue Legenden</li>
    </dl>

    <p class="mb-1">Aussparungsbeschriftungen</p>
    <p>Automatische Dimensions- und Höhenangaben für alle Gewerke.</p>

    <p class="mb-1">Tab-Bezeichnungen</p>
    <p>Kürzere Bezeichnungen für die einzelnen Katalog-tabs ermöglichen die Anzeige von mehr Plug-ins in der Revit-Menüleiste ohne Ausklappen des Menüs.</p>
</div>

<div class="article-header-small">FEHLERBEHEBUNG</div>
<div class="article-content">
    <p>Performanceprobleme</p>
    <p>Wenn der FamilyBrowser installiert, aber seit dem Öffnen von Revit noch nicht benutzt wurde, ist es in der Vergangenheit zu langen Ladezeiten beim Wechseln zwischen Projekten oder dem Versuch, eine Bauteilfamilie zu bearbeiten, gekommen. Der Fehler wurde behoben, die Performance ist wieder normal.</p>
</div>

