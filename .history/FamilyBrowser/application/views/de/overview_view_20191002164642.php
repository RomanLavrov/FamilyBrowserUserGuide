      <p>RevitFamilyBrowser ist ein Revit-Plugin, welches den Zugriff auf Familien von BUILDing360-Bauteilkatalog
            ermöglicht. Mit Hilfe vom Filter können Familien ins Projekt schnell hinzugefügt werden.
      </p>

